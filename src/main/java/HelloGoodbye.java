public class HelloGoodbye {
    // 为了测试通过Coursera
    // 很质朴地用一系列打印来实现
    public static void main(String[] args) {
        System.out.print("Hello ");
        System.out.print(args[0]);
        System.out.print(" and ");
        System.out.print(args[1]);
        System.out.println(".");

        System.out.print("Goodbye ");
        System.out.print(args[1]);
        System.out.print(" and ");
        System.out.print(args[0]);
        System.out.println(".");
    }
}
